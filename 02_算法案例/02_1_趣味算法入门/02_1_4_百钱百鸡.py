"""
公鸡一只5钱，母鸡一只3钱，小鸡三只1钱，仅有百钱买百鸡问各有几数
    三个未知数，两个方程，无法直接求解，所以采用遍历循环。
"""
#设公鸡有x个(x<=20),母鸡有y个(y<=33),小鸡有z个
x=0
while x<=20:
    y=0
    while (y <=(100-5*x)/3):
        z=0
        while z<100:
            if(x+y+z==100):
                if(5*x+3*y+z/3==100):
                    print("公鸡的数量为%d，母鸡的数量为%d，小鸡的数量为%d"%(x,y,z))
            z=z+1
        y=y+1
    x=x+1